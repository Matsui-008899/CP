package com.example.calendarapplication.Food;

public class FoodRowData {
    private String startView;
    private String endView;
    private String taskView;
    private int tag;

    public String getStartView() {
        return startView;
    }

    public void setStartView(String startView) {
        this.startView = startView;
    }

    public String getEndView() {
        return endView;
    }

    public void setEndView(String endView) {
        this.endView = endView;
    }

    public String getTaskView() {
        return taskView;
    }

    public void setTaskView(String taskView) {
        this.taskView = taskView;
    }

    public int getTag() {
        return tag;
    }

    public void setTag(int tag) {
        this.tag = tag;
    }
}
